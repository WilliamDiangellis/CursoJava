// Todas as afirmações sobre arrays são corretas, exceto:

// A)
// É uma estrutura de dados.
// É correto. Array é uma estrutura de dados.

// B)
// Um array não é um objeto.
// Não é verdadeiro. Repare que usamos new para criar um array! Arrays também
// são objetos.

// C)
// Um array é sempre inicializado com os valores padrões.
// É verdade, pois cada posição do array é inicializada com o valor padrão. Qual
// é esse valor padrão? O tipo do array define. Por exemplo, no array do tipo
// int o valor padrão é 0, no double o valor padrão é 0.0.

// D)
// Um array é sempre zero-based (o primeiro elemento se encontra no index 0).
// É correto, a primeira posição do array é sempre indicada pelo index 0.