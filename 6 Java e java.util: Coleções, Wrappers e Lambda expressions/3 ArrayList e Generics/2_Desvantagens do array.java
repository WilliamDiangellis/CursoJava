// Quais foram as desvantagens apontadas ao usar arrays?

// A)
// Sintaxe fora do padrão "OO Java"
// Correto. Os arrays realmente têm a sua própria sintaxe, o que dificulta a
// legibilidade do código.

// B)
// Array tem um tamanho fixo (não pode crescer dinamicamente)
// Correto. Uma vez criado, o array sempre terá o mesmo tamanho de elementos.
// Isso é muito ruim quando não sabemos exatamente quantos elementos precisamos
// guardar.

// C)
// Array não sabe quantas posições estão ocupadas (apenas tamanho total)
// Correto. Isso é uma grande desvantagem. Não queremos saber quantos elementos
// um array pode ter, mas sim quantos elementos realmente já existem no array.

// D)
// Array é uma estrutura considerada lenta
// Errado, alias é bem contrário, pensando no desempenho o array é a estrutura
// que se destaca.