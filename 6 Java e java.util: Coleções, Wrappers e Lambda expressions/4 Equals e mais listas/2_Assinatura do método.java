// O método equals, junto aos métodos toString e hashCode, é um dos métodos
// fundamentais da classe Object.

// Qual é a assinatura correta desse método?

// A)
// public boolean equals()
// Errado, falta o parâmetro.

// B)
// public void equals(Object ref)
// Errado, deve devolver um boolean.

// C)
// public boolean equals(Object ref)
// Correto, é publico, devolve boolean e recebe um Object.

// D)
// boolean equals(Object ref)
// Errado, o método deve ser publico.