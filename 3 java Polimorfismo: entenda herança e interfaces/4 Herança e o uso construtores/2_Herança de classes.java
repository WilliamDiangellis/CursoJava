// atributos da hierarquia).

// B)
// Quando uma classe herda de outra, ela recebe também seus métodos.
// É verdadeira, herdamos os métodos.

// C)
// Quando uma classe herda de outra, ela recebe também seus construtores
// automaticamente.
// Não é verdade, pois recebe apenas seus métodos e atributos. Lembra-se não tem
