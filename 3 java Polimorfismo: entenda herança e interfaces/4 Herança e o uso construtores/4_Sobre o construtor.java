// Aprendemos que a construção de um objeto é baseada em seu(s) construtor(es).

// Qual das alternativas abaixo é a correta?

// A)
// Só podemos declarar um construtor por classe, essa é uma limitação do próprio
// Java, que deixaremos de ter a partir do Java 8.
// Errado, podemos declarar quantos construtores que quisermos.

// B)
// O construtor default do Java deixa de existir a partir do momento que algum
// outro é declarado na classe.
// Correto, assim que criarmos o nosso próprio construtor, o construtor default
// (sem parâmetros) deixa de existir, No entanto, nada impede adicionar o
// construtor default explicitamente.

// C)
// Todas as classes que possuem atributos, recebem sempre esses valores via
// construtores.
// Errado, além do construtor, pode ser usado um método (setter), por exemplo.

// D)
// Todas os objetos podem ser instanciados, sem a passagem de parâmetros no
// construtor. Independentemente se a classe declara algum outro.
// Errado, pois só podemos construir um objeto sem a passagem de parâmetros no
// construtor se existir o construtor sem parâmetros.

