// O que é verdade sobre classes abstratas? Selecione todas as afirmações
// verdadeiras:

// A)
// Podem ter métodos concretos (com implementação)
// Correto, como podem ter atributos também podem ter métodos concretos!

// B)
// Não podem ser instanciadas
// Correto, pois o que é abstrato (a classe) não pode se tornar concreto
// (objeto). Por isso não podemos instanciar objetos de uma classe abstrata.

// C)
// Podem ter atributos
// Correto, podemos sim ter atributos! Uma classes abstrata é uma classe normal,
// só não pode instanciar e pode ter métodos abstratos. O resto continua
// valendo!

// D)
// Podem ter métodos abstratos (sem implementação)
// Correto, como vimos uma classe abstrata pode ter métodos sem implementação.
// Dessa forma obrigamos um filho implementar o método.

// E)
// Não podem ter construtores
// Errado, pois podem sim ter construtores! Uma classes abstrata é uma classe
// normal, só não pode instanciar e pode ter métodos abstratos. O resto continua
// valendo!