// Sobre classes e métodos abstratos, das afirmativas abaixo, qual delas é
// FALSA?

// A)
// Classes abstratas são úteis quando queremos utilizar comportamentos e
// atributos base em classes com comportamentos em comum.
// Verdadeira. Na verdade esta alternativa está correta! Os benefícios da
// herança continuam valendo!

// B)
// Classes e métodos abstratos consomem menos memória e por conta disso melhoram
// o desempenho do nosso programa.
// Falsa. Essa afirmação realmente é errada. Classes e métodos abstratos não tem
// relação direta com consumo de memória.

// C)
// Usamos métodos abstratos quando queremos "forçar" que um filho concreto
// (classe concreta) implemente um método.
