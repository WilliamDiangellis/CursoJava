// Qual é a sintaxe correta para estender uma classe no Java?

// A)
// class Carro : Veiculo {}
// Errado, a sintaxe apresentada nessa alternativa é da linguagem C# do mundo
// .NET !
// Lembre-se, no mundo Java usamos a palavra chave extends entre as classes.

// B)
// class Carro(Veiculo)
// Errado, a sintaxe apresentada nessa alternativa é da linguagem Python!
// Lembre-se, no mundo Java usamos a palavra chave extends entre as classes.

// C)
// class Carro extends Veiculo {}
// Correto, no mundo Java usa-se a palavra chave extends. E só por curiosidade,
// todas as outras afirmativas são exemplos de herança em outras linguagens:

// Herança em C#: class Carro : Veiculo {}
// Herança em Ruby: class Carro < Veiculo
// Herança em Python: class Carro(Veiculo)

// D)
// class Carro < Veiculo
// Errado, a sintaxe apresentada nessa alternativa é da linguagem Ruby!
