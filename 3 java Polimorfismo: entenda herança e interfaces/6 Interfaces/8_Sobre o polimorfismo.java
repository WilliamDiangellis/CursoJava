// Quanto ao conceito do Polimorfismo marque as alternativas corretas:

// A)
// É a capacidade de um objeto ser referenciado por vários tipos.
// Correta, podemos comunicar com um objeto através de tipos de variáveis
// diferentes.
// Por exemplo, se existir uma classe Gerente que seja filha de Funcionario, um
// objeto do tipo Gerente pode ser referenciado com o tipo Funcionario também.

// B)
// É a capacidade de um objeto chamar métodos do seu pai usando super.
// Errado, através super deixamos claro que queremos acessar um membro da classe
// mãe.

// C)
// Só temos polimorfismo quando uma classe extende de outra, ou seja, apenas via
// herança.
// Errado, vimos que as interfaces são uma alternativa também para ter
// polimorfismo.

// D)
// Temos polimorfismo quando uma classe extende de outra ou também quando uma
// classe implementa uma interface.
