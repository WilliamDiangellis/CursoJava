// A)
// Temos quatro modificadores: private, protected, default e public. Onde a
// ordem do MAIS restritivo para o MENOS restritivo é: private, default,
// protected e public.
// Correto.

// B)
// Temos quatro modificadores: private, protected, default e public. Onde a
// ordem do MAIS restritivo para o MENOS restritivo é: public, protected,
// default e private.
// Errado, justamente contrário.

// C)
// São apenas dois, public ou private. Enquanto o primeiro permite acesso
// externo, o segundo restringe o acesso a somente quem declarou.
// Errado, temos 4 modificadores de acesso.

// D)
// O package private do Java tem o mesmo comportamento do public. Portanto usar
// a palavra reservada public é apenas preciosismo do desenvolvedor.
// Errado, public significa visível em qualquer lugar, enquanto package private
// significa visível apenas dentro do mesmo package.