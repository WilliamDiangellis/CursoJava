// O que aprendemos sobre métodos?

// A)
// É possível que um método não tenha nenhum parâmetro.
// Correto, um método pode ter nenhum, um ou mais parâmetros.

// B)
// Por convenção, o nome do método no mundo Java deve começar com letra
// minúscula.
// Correto, exemplo de nome dos métodos são:

// transfere
// transferePara
// transfereParaOutroTitular

// Repare que todos os nomes começam com letra minúscula e depois usam
// "CamelCase".

// C)
// Um método define o comportamento ou a maneira de fazer algo.
// Correto, esse é o objetivo de métodos, definir o que um objeto sabe fazer.
// O comportamento é implementado dentro do método.

// D)
// Um método não precisa definir a saída. Se não tiver um valor de retorno,
// basta escrever apenas o nome de método.
// Errado, qualquer método no mundo Java precisa definir o retorno. Isso também
// é quando não queremos retornar nada, pois devemos usar a palavra chave void.